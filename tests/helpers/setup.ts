/** Initialize an SSS-1 stablecoin. */
export async function initSSS1(ctx: TestContext, name = "Test USD", symbol = "TUSD"): Promise<void> {
  await ctx.program.methods
    .initialize(name, symbol, "", 6, false, false, false)
    .accounts({
      payer: ctx.authority.publicKey,
      mint: ctx.mintKeypair.publicKey,
      stablecoinConfig: ctx.configPda,
      roleManager: ctx.rolesPda,
      tokenProgram: TOKEN_2022_PROGRAM_ID,
      systemProgram: SystemProgram.programId,
      rent: SYSVAR_RENT_PUBKEY,
    })
    .signers([ctx.authority, ctx.mintKeypair])
    .rpc();
}

/** Initialize an SSS-2 stablecoin. */
export async function initSSS2(ctx: TestContext, name = "Compliance USD", symbol = "CUSD"): Promise<void> {
  await ctx.program.methods
    .initialize(name, symbol, "", 6, true, true, false)
    .accounts({
      payer: ctx.authority.publicKey,
      mint: ctx.mintKeypair.publicKey,
      stablecoinConfig: ctx.configPda,
      roleManager: ctx.rolesPda,
      tokenProgram: TOKEN_2022_PROGRAM_ID,
      systemProgram: SystemProgram.programId,
      rent: SYSVAR_RENT_PUBKEY,
    })
    .signers([ctx.authority, ctx.mintKeypair])
    .rpc();

  // Initialize hook extra account meta list
  const [extraMetaList] = findExtraMetaListPda(
    ctx.mintKeypair.publicKey,
    ctx.hookProgram.programId
  );

  await ctx.program.methods
    .initHookAccounts()
    .accounts({
      payer: ctx.authority.publicKey,
      stablecoinConfig: ctx.configPda,
      mint: ctx.mintKeypair.publicKey,
      extraAccountMetaList: extraMetaList,
      hookProgram: ctx.hookProgram.programId,
      systemProgram: SystemProgram.programId,
    })
    .signers([ctx.authority])
    .rpc();
}

/** Create an ATA for a user if it doesn't exist, then mint tokens to it. */
export async function mintTo(
  ctx: TestContext,
  recipient: PublicKey,
  amount: number,
  minter?: Keypair
): Promise<string> {
  const signer = minter ?? ctx.authority;
  const ata = getAssociatedTokenAddressSync(
    ctx.mintKeypair.publicKey, recipient, true, TOKEN_2022_PROGRAM_ID
  );

  // Create ATA if needed
  const ataInfo = await ctx.connection.getAccountInfo(ata);
  const preIxs = ataInfo
    ? []
    : [createAssociatedTokenAccountInstruction(
        signer.publicKey, ata, recipient,
        ctx.mintKeypair.publicKey, TOKEN_2022_PROGRAM_ID,
      )];

  const sig = await ctx.program.methods
    .mintTokens(new BN(amount))
    .accounts({
      authority: signer.publicKey,
      stablecoinConfig: ctx.configPda,
      roleManager: ctx.rolesPda,
      mint: ctx.mintKeypair.publicKey,
      recipientTokenAccount: ata,
      tokenProgram: TOKEN_2022_PROGRAM_ID,
    })
    .preInstructions(preIxs)
    .signers([signer])
    .rpc();

  await ctx.connection.confirmTransaction(sig, "confirmed");
  return sig;
}

/** Get token balance for a wallet's ATA. */
export async function getBalance(ctx: TestContext, owner: PublicKey): Promise<bigint> {
  const ata = getAssociatedTokenAddressSync(
    ctx.mintKeypair.publicKey, owner, true, TOKEN_2022_PROGRAM_ID
  );
  try {
    const account = await getAccount(ctx.connection, ata, "confirmed", TOKEN_2022_PROGRAM_ID);
    return account.amount;
  } catch {
    return BigInt(0);
  }
}

/**
 * Execute a Token-2022 transfer_checked on an SSS-2 mint with transfer-hook
 * metas resolved from the on-chain ExtraAccountMetaList.
 */
export async function transferWithHook(
  ctx: TestContext,
  sourceTokenAccount: PublicKey,
  destTokenAccount: PublicKey,
  owner: Keypair,
  amount: number,
  decimals: number
): Promise<void> {
  const ix = createTransferCheckedInstruction(
    sourceTokenAccount,
    ctx.mintKeypair.publicKey,
    destTokenAccount,
    owner.publicKey,
    amount,
    decimals,
    [],
    TOKEN_2022_PROGRAM_ID
  );

  await addExtraAccountMetasForExecute(
    ctx.connection,
    ix,
    ctx.hookProgram.programId,
    sourceTokenAccount,
    ctx.mintKeypair.publicKey,
    destTokenAccount,
    owner.publicKey,
    BigInt(amount),
    "confirmed"
  );

  const tx = new Transaction().add(ix);
  await sendAndConfirmTransaction(ctx.connection, tx, [owner], {
    commitment: "confirmed",
  });
}

/** Helper to expect a transaction to fail with a specific error code. */
export async function expectError(
  fn: () => Promise<any>,
  errorCode: string
): Promise<void> {
  try {
    await fn();
    assert.fail("Expected transaction to fail");
  } catch (err: any) {
    const errStr = JSON.stringify(err);
    assert.include(errStr, errorCode, `Expected error ${errorCode} but got: ${err.message}`);
  }
}
