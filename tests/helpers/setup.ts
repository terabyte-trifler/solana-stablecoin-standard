/** Everything a test suite needs. Built by `createTestContext`. */
export interface TestContext {
  provider: AnchorProvider;
  connection: Connection;
  program: Program<SssToken>;
  hookProgram: Program<SssTransferHook>;

  // Keypairs
  authority: Keypair;       // master authority / payer
  mintKeypair: Keypair;     // the Token-2022 mint
  minterA: Keypair;         // minter role
  minterB: Keypair;         // second minter
  burner: Keypair;          // burner role
  pauser: Keypair;          // pauser role
  blacklister: Keypair;     // blacklister role (SSS-2)
  seizer: Keypair;          // seizer role (SSS-2)
  userA: Keypair;           // regular user A
  userB: Keypair;           // regular user B
  unauthorized: Keypair;    // has no roles

  // Derived PDAs
  configPda: PublicKey;
  rolesPda: PublicKey;

  // Token accounts (set after init + ATA creation)
  authorityAta: PublicKey;
  userAAta: PublicKey;
  userBAta: PublicKey;
}

/**
 * Create a full test context with funded keypairs and derived PDAs.
 * Call this in `before()` for each test suite.
 */
export async function createTestContext(): Promise<TestContext> {
  const envProvider = AnchorProvider.env();
  const provider = new AnchorProvider(
    envProvider.connection,
    envProvider.wallet,
    { commitment: "confirmed", preflightCommitment: "confirmed" }
  );
  anchor.setProvider(provider);

  const program = anchor.workspace.SssToken as Program<SssToken>;
  const hookProgram = anchor.workspace.SssTransferHook as Program<SssTransferHook>;

  // Generate all keypairs
  const authority = Keypair.generate();
  const mintKeypair = Keypair.generate();
  const minterA = Keypair.generate();
  const minterB = Keypair.generate();
  const burner = Keypair.generate();
  const pauser = Keypair.generate();
  const blacklister = Keypair.generate();
  const seizer = Keypair.generate();
  const userA = Keypair.generate();
  const userB = Keypair.generate();
  const unauthorized = Keypair.generate();

  // Airdrop SOL to all signers
  const connection = provider.connection;
  const keypairsToFund = [
    authority, minterA, minterB, burner, pauser,
    blacklister, seizer, userA, userB, unauthorized,
  ];

  for (const kp of keypairsToFund) {
    const sig = await connection.requestAirdrop(kp.publicKey, 10 * LAMPORTS_PER_SOL);
    await connection.confirmTransaction(sig);
  }

  // Derive PDAs
  const [configPda] = findConfigPda(mintKeypair.publicKey, program.programId);
  const [rolesPda] = findRolesPda(configPda, program.programId);

  // Derive ATAs (they'll be created during tests)
  const authorityAta = getAssociatedTokenAddressSync(
    mintKeypair.publicKey, authority.publicKey, true, TOKEN_2022_PROGRAM_ID
  );
  const userAAta = getAssociatedTokenAddressSync(
    mintKeypair.publicKey, userA.publicKey, true, TOKEN_2022_PROGRAM_ID
  );
  const userBAta = getAssociatedTokenAddressSync(
    mintKeypair.publicKey, userB.publicKey, true, TOKEN_2022_PROGRAM_ID
  );

  return {
    provider, connection, program, hookProgram,
    authority, mintKeypair,
    minterA, minterB, burner, pauser, blacklister, seizer,
    userA, userB, unauthorized,
    configPda, rolesPda,
    authorityAta, userAAta, userBAta,
  };
}

